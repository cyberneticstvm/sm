    {
        $this->validate($request, [
            'menu_type_id' => 'required',
            'page_id' => 'required',
            'order_by' => 'required',
        ]);
        $input = $request->all();
        $input['publish'] = 1;
        $menu = Menu::create($input);
        return redirect()->route('admin.menu-list')
                        ->with('success','Menu created successfully');
